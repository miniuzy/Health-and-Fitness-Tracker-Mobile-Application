package com.example.mohammeduzair.androidapp.utility;

public class Userinformation {

    private String Calories;
    private String BMI;
    private String Steps;
    private String Email;
    private String Calories_Burned;
    private String Date;
    private String Distance;
    private String Feeling;
    private String Time_Slept;
    private String HeartLED;

    public Userinformation(){

    }

    public String getHeartLED() {
        return HeartLED;
    }

    public void setHeartLED(String heartLED) {
        HeartLED = heartLED;
    }

    public String getCalories() {
        return Calories;
    }

    public void setCalories(String calories) {
        Calories = calories;
    }

    public String getCalories_Burned() {
        return Calories_Burned;
    }

    public void setCalories_Burned(String calories_Burned) {
        Calories_Burned = calories_Burned;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }

    public String getDistance() {
        return Distance;
    }

    public void setDistance(String distance) {
        Distance = distance;
    }

    public String getFeeling() {
        return Feeling;
    }

    public void setFeeling(String feeling) {
        Feeling = feeling;
    }

    public String getTime_Slept() {
        return Time_Slept;
    }

    public void setTime_Slept(String time_Slept) {
        Time_Slept = time_Slept;
    }

    public String getBMI() {
        return BMI;
    }

    public void setBMI(String BMI) {
        this.BMI = BMI;
    }

    public String getSteps() {
        return Steps;
    }

    public void setSteps(String steps) {
        Steps = steps;
    }
    public String getEmail() {
        return Email;
    }

    public void setEmail(String email) {
        Email = email;
    }
}
